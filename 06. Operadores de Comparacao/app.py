# Operadores de Comparação em Python

# ==  Igualdade (compara se dois valores são iguais)
# !=  Diferença (compara se dois valores são diferentes)
# >   Maior que
# <   Menor que
# >=  Maior ou igual
# <=  Menor ou igual

# Resultado de uma comparação sempre é booleano: True ou False
numero1 = 10
numero2 = 11

resultado = numero2 == numero1
print(type(resultado))

